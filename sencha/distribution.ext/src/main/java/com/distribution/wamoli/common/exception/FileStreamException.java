package com.distribution.wamoli.common.exception;


public class FileStreamException extends ApplicationException {
	private static final long serialVersionUID = 1L;
	
	
	public FileStreamException() {
		super();
	}
	
	public FileStreamException(String message) {
		super(message);
	}
	
	public FileStreamException(Throwable cause) {
		super(cause);
	}
	
	public FileStreamException(String message, Throwable cause) {
		super(message, cause);
	}
	
	

	

}
