package com.distribution.wamoli.config;

import com.codahale.metrics.MetricRegistry;
import com.codahale.metrics.servlet.InstrumentedFilter;
import com.codahale.metrics.servlets.MetricsServlet;
import com.distribution.wamoli.config.auth.ValidLoginFilter;
import com.distribution.wamoli.security.CorsAwareAuthenticationFilter;
import com.distribution.wamoli.web.filter.CachingHttpHeadersFilter;
