package com.distribution.wamoli.common.exception;

public class ZipException extends ApplicationRuntimeException{
	private static final long serialVersionUID = 1L;
	
	
	public ZipException() {
		super();
	}
	
	public ZipException(String message) {
		super(message);
	}
	
	public ZipException(Throwable cause) {
		super(cause);
	}
	
	public ZipException(String message, Throwable cause) {
		super(message, cause);
	}
	
	

	

}

